wishlistRouter.post('/toggle', async (req, res) => {
    const { userId, tourId } = req.body;
    try {
        const user = await User.findById(userId);
        if (!user) {
            return res.status(404).json({ success: false, message: "Kh√¥ng t√¨m th·∫•y ng∆∞·ªùi d√πng." });
        }

        const tourIndex = user.wishlist.indexOf(tourId);
        if (tourIndex > -1) {
            // N·∫øu tour ƒë√£ c√≥, x√≥a ƒëi
            user.wishlist.splice(tourIndex, 1);
        } else {
            // N·∫øu tour ch∆∞a c√≥, th√™m v√†o
            user.wishlist.push(tourId);
        }
        await user.save();
        
        // Tr·∫£ v·ªÅ danh s√°ch wishlist ƒë√£ ƒë∆∞·ª£c c·∫≠p nh·∫≠t ƒë·∫ßy ƒë·ªß th√¥ng tin
        const updatedUser = await User.findById(userId).populate({
            path: 'wishlist',
            model: 'Tour',
             populate: {
                path: 'destination',
                model: 'Destination'
            }
        });
        res.status(200).json({ success: true, data: updatedUser.wishlist });

    } catch (error) {
        console.error("L·ªói toggle wishlist:", error);
        res.status(500).json({ success: false, message: "L·ªói m√°y ch·ªß." });
    }
});

